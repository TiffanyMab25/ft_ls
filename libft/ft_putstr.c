#include "libft.h"

void	ft_putstr(char const *str)
{
	int i;

	i = 0;
	if (str == NULL)
		write(1, "NULL", 4);
	else
		while (str[i] != '\0')
		{
			ft_putchar(str[i]);
			i++;
		}
}
